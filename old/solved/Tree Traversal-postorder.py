# Definition for a binary tree node.
from collections import deque
class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right
class Solution:
    def postorderTraversal(self, root: TreeNode) -> [int]:
        if not root:
            return []

        q = deque()
        res = []

        while q or root:
            if root:
                res.append(root.val)
                q.append(root)
                root = root.right
            else:
                node = q.pop()
                root = node.left

        return res[::-1]